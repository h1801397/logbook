# I use variable a to give a value iserted by user
a = int(input("Enter an integer number: "))

# using modulo (%) to print the remainder after the division
print("The remainder result after dividing %d by \
7 is: %d " %(a, a%7))


# using floor division(//)
print("Number of times  that 7 fits into %d is %d " \
%(a, a//7))
